// ConfluenceProperties is a typed property bag attached to a Confluence page.
// SourceBridge uses it to store the sourcebridge_page_id and last-sync metadata.
type ConfluenceProperties map[string]string

// ConfluenceClient is the port interface for interacting with the Confluence
// REST API. Real implementations call the Confluence Cloud/Server REST API;
// [MemoryConfluenceClient] is the in-memory fake for tests.
//
// All methods receive a context so real implementations can respect
// cancellation and deadlines.
type ConfluenceClient interface {
	// GetPage fetches a page by its SourceBridge external ID.
	// Returns (xhtml, metadata, nil) when the page exists.
	// Returns (nil, nil, nil) when no page with externalID exists yet.
	GetPage(ctx context.Context, externalID string) (xhtml []byte, metadata ConfluenceProperties, err error)

	// UpsertPage creates or updates the page identified by externalID with the
	// given XHTML body and metadata properties. The implementation is
	// responsible for translating externalID to a Confluence page ID on update.
	UpsertPage(ctx context.Context, externalID string, xhtml []byte, metadata ConfluenceProperties) error

	// GetBlockByExternalID fetches the XHTML body of a specific block within a
	// page, identified by the block's SourceBridge block ID stored in the macro
	// parameter. Returns (xhtml, true, nil) when the block is found.
	// Returns (nil, false, nil) when the block does not exist in the page.
	GetBlockByExternalID(ctx context.Context, pageExternalID string, blockExternalID ast.BlockID) (xhtml []byte, ok bool, err error)

	// ListPagesByExternalIDPrefix returns the external IDs of all Confluence
	// pages whose sourcebridge_page_id property starts with prefix. Used by
	// the orphan-cleanup pass to find pages from a specific repo.
	// Implementations may return an empty slice rather than an error when the
	// capability is not available.
	ListPagesByExternalIDPrefix(ctx context.Context, prefix string) ([]string, error)

	// DeletePage permanently deletes the Confluence page identified by
	// externalID. Returns nil when the page did not exist (idempotent).
	DeletePage(ctx context.Context, externalID string) error

	// EnsurePage creates a stub page (typically a root or section page) if one
	// does not already exist with the given external ID, and returns the
	// Confluence numeric page ID either way.
	//
	// parentExternalID may be empty when the page should live at the space root.
	// When non-empty the parent page must already exist (caller's responsibility).
	// body is the initial XHTML body for new pages; existing pages are not updated.
	EnsurePage(ctx context.Context, externalID, title string, parentExternalID string, body []byte) (string, error)
}

