// ConfluenceWriter
// ─────────────────────────────────────────────────────────────────────────────

// ConfluenceWriter is the Confluence storage-XHTML sink adapter.
//
// Call [WritePage] to do the full read-diff-write cycle:
//  1. Ensure the root and Architecture section pages exist (once per writer lifetime).
//  2. Fetch the existing Confluence page via the [ConfluenceClient].
//  3. Parse the existing XHTML to recover block IDs and ownership.
//  4. Run block-level reconciliation.
//  5. Write only changed generated blocks; preserve human-edited blocks.
//  6. Upsert the composed XHTML via the [ConfluenceClient].
//
// The caller must compose canonical + overlay[sink] via [ast.ComposeForSink]
// before calling WritePage when the sink has a local_to_sink policy.
type ConfluenceWriter struct {
	client ConfluenceClient
	cfg    ConfluenceWriterConfig

	// hierarchyOnce guards the one-time creation of the root and Architecture
	// section pages. Concurrent WritePage calls will all block until the first
	// caller has created the pages; subsequent callers proceed immediately.
	hierarchyOnce sync.Once
	hierarchyErr  error // non-nil when EnsurePage calls fail during setup
}

// NewConfluenceWriter creates a [ConfluenceWriter] using the given client.
func NewConfluenceWriter(client ConfluenceClient, cfg ConfluenceWriterConfig) *ConfluenceWriter {
	return &ConfluenceWriter{client: client, cfg: cfg}
}

// ensureHierarchy creates the root and Architecture section pages exactly once
// per writer lifetime. It is called from WritePage under a sync.Once so
// concurrent page writes block until the hierarchy exists.
//
// When cfg.RepoID is empty the hierarchy is disabled and this is a no-op.
func (cw *ConfluenceWriter) ensureHierarchy(ctx context.Context) error {
	cw.hierarchyOnce.Do(func() {
		rootID, archID := cw.cfg.hierarchyExternalIDs()
		if rootID == "" {
			return // hierarchy disabled (no RepoID configured)
		}

		repoName := cw.cfg.RepoName
		if repoName == "" {
			repoName = cw.cfg.RepoID
		}

		rootBody := fmt.Appendf(nil,
			`<p>This is the auto-generated SourceBridge wiki for %s. Pages are kept in sync as the codebase evolves.</p>`+"\n"+
				`<ac:structured-macro ac:name="children" />`,
			xmlEscape(repoName),
		)

		// Root page lives at the configured space root (no parent external ID).
		_, err := cw.client.EnsurePage(ctx, rootID, repoName+" Living Wiki", "", rootBody)
		if err != nil {
			cw.hierarchyErr = fmt.Errorf("confluence_writer: ensure root page: %w", err)
			return
		}

		archBody := fmt.Appendf(nil,
			`<p>Architecture documentation for %s.</p>`+"\n"+
				`<ac:structured-macro ac:name="children" />`,
			xmlEscape(repoName),
		)

		// Architecture section is parented to the root.
		_, err = cw.client.EnsurePage(ctx, archID, "Architecture", rootID, archBody)
		if err != nil {
			cw.hierarchyErr = fmt.Errorf("confluence_writer: ensure architecture section: %w", err)
		}
	})
	return cw.hierarchyErr
}

// parentExternalIDForPage determines which parent page a new content page
// should be created under, expressed as a SourceBridge external ID.
//
// Routing rules (v1):
//   - Pages whose ID matches <repoID>.arch.* → Architecture section
//   - All other pages → root page
//
// Returns "" when hierarchy is disabled (no RepoID configured).
func (cw *ConfluenceWriter) parentExternalIDForPage(pageID string) string {
	rootID, archID := cw.cfg.hierarchyExternalIDs()
	if rootID == "" {
		return ""
	}
	prefix := cw.cfg.RepoID + ".arch."
	if strings.HasPrefix(pageID, prefix) {
		return archID
	}
	return rootID
}

// WritePage performs the read-diff-write cycle for one page.
//
// The incoming page's blocks are expected to carry stable IDs already
// assigned by the orchestrator (canonical AST or proposed AST).
//
// Algorithm:
//  1. Ensure root and Architecture section pages exist (once per writer lifetime).
//  2. Fetch the existing Confluence page.
//  3. Parse existing blocks to build a map of blockID → existing block.
//  4. For each incoming block:
//     - If an existing block with the same ID is human-edited or human-only,
//       preserve its content and owner (the human edit wins).
//     - Otherwise use the incoming block's content.
//  5. Render and upsert, injecting the per-page parent external ID so new pages
//     land under the correct section rather than the space root.
//
// Caller responsibility: pass in the result of [ast.ComposeForSink] when the
// Confluence sink has a local_to_sink overlay, so that per-sink divergent
// content is included in the write.
func (cw *ConfluenceWriter) WritePage(ctx context.Context, page ast.Page) error {
	// Step 1: Ensure hierarchy pages exist (idempotent, runs once per writer).
	if err := cw.ensureHierarchy(ctx); err != nil {
		return err
	}

	// Step 2: Fetch existing page.
	existingXHTML, _, err := cw.client.GetPage(ctx, page.ID)
	if err != nil {
		return fmt.Errorf("confluence_writer: GetPage %q: %w", page.ID, err)
	}

	// Step 3: Parse existing blocks to recover ownership by block ID.
	existingByID := make(map[ast.BlockID]ast.Block)
	if len(existingXHTML) > 0 {
		for _, b := range confluenceXHTMLToBlocks(existingXHTML) {
			existingByID[b.ID] = b
		}
	}

	// Step 4: Compose the output blocks, preserving human-edited content.
	output := make([]ast.Block, len(page.Blocks))
	for i, blk := range page.Blocks {
		output[i] = blk
		if existing, ok := existingByID[blk.ID]; ok {
			if existing.Owner == ast.OwnerHumanEdited || existing.Owner == ast.OwnerHumanOnly {
				// Human edit wins — preserve the existing content and owner.
				output[i].Content = existing.Content
				output[i].Owner = existing.Owner
			}
		}
	}

	// Step 5: Render to XHTML and upsert.
	var buf bytes.Buffer
	if err := writeConfluencePage(&buf, ast.Page{
		ID:         page.ID,
		Manifest:   page.Manifest,
		Blocks:     output,
		Provenance: page.Provenance,
	}); err != nil {
		return fmt.Errorf("confluence_writer: render %q: %w", page.ID, err)
	}

	props := ConfluenceProperties{
		"sourcebridge_page_id": page.ID,
		"space_key":            cw.cfg.SpaceKey,
		// Title is consumed by the HTTP client when creating/updating the
		// Confluence page. Sinks that don't read this property (e.g. the
		// in-memory fake) just store it. We prefer the page's first heading
		// because that's the text humans authored as the natural title;
		// otherwise fall back to a deterministic humanization of the page ID.
		propConfluenceTitle: deriveConfluenceTitle(page),
	}

	// Inject the per-page parent so new pages land under the correct section
	// rather than the configured top-level parentPageID. The HTTP client reads
	// this and resolves it to a Confluence numeric ID before the CREATE call.
	if parentExtID := cw.parentExternalIDForPage(page.ID); parentExtID != "" {
		props[propParentExternalID] = parentExtID
	}

	if err := cw.client.UpsertPage(ctx, page.ID, buf.Bytes(), props); err != nil {
		return fmt.Errorf("confluence_writer: UpsertPage %q: %w", page.ID, err)
	}

	return nil
}

// propConfluenceTitle is the property key on UpsertPage's metadata that the
// HTTP client uses as the Confluence page title. It is not persisted as a
// Confluence content-property — the HTTP layer pulls it out before the
// remaining keys are written through to Confluence.
const propConfluenceTitle = "sb_confluence_title"

// deriveConfluenceTitle returns the page's Confluence title as a deterministic
// humanization of page.ID via HumanizePageID. Using a stable ID-derived title
// (rather than the first heading) ensures cross-page links always resolve —
// a heading can be edited by a human but the page ID never changes.
func deriveConfluenceTitle(page ast.Page) string {
	return HumanizePageID(page.ID)
}

// HumanizePageID converts a SourceBridge page ID into a human-friendly title.
// The transformation is deterministic so create-time and search-time produce
// the same string, which is required for cross-page <ac:link> resolution.
//
// Special cases (matched after stripping a leading UUID repo-id segment):
//   - "arch.<rest>" → "Architecture: <rest with . replaced by />"
//   - "system_overview" → "System Overview"
//   - "glossary" → "Glossary"
//   - "api_reference" → "API Reference"
//   - Other single-segment IDs → title-case, underscore→space, common acronyms uppercased
//   - Multi-segment IDs → join with " · ", each segment title-cased
func HumanizePageID(externalID string) string {
	if externalID == "" {
		return externalID
	}
	parts := strings.Split(externalID, ".")
	// Drop a leading repo-id segment when it looks like a UUID (contains 4
	// hyphens) — repo IDs would otherwise dominate the visible title.
	if len(parts) > 1 && strings.Count(parts[0], "-") == 4 {
		parts = parts[1:]
	}
	if len(parts) == 0 {
		return externalID
	}

	// Special case: arch.* → "Architecture: <path with / separators>"
	if parts[0] == "arch" && len(parts) > 1 {
		rest := strings.Join(parts[1:], "/")
		return "Architecture: " + rest
	}

	// Special case: well-known single-segment IDs.
	if len(parts) == 1 {
		switch parts[0] {
		case "system_overview":
			return "System Overview"
		case "glossary":
			return "Glossary"
		case "api_reference":
			return "API Reference"
		}
	}

	// General: title-case each segment with acronym uppercasing.
	pretty := make([]string, 0, len(parts))
	for _, p := range parts {
		pretty = append(pretty, humanizeSegment(p))
	}
	return strings.Join(pretty, " · ")
}

// humanizeSegment converts a single page-ID segment (e.g. "api_reference")
// to a human-friendly label ("API Reference"). It:
//  1. Replaces underscores with spaces.
//  2. Uppercases each word if it is a known acronym; otherwise title-cases it.
func humanizeSegment(s string) string {
	words := strings.Fields(strings.ReplaceAll(s, "_", " "))
	for i, w := range words {
		if isAcronym(strings.ToLower(w)) {
			words[i] = strings.ToUpper(w)
		} else if len(w) > 0 {
			words[i] = strings.ToUpper(w[:1]) + w[1:]
		}
	}
	return strings.Join(words, " ")
}

// isAcronym returns true when w (already lowercased) is a well-known acronym
// that should be rendered in full uppercase.
func isAcronym(w string) bool {
	switch w {
	case "api", "db", "id", "url", "http", "sql", "xml", "json",
		"yaml", "aws", "gcp", "dns", "tls", "ui", "iam", "rpc",
		"grpc", "jwt", "oauth", "oidc", "ci", "cd", "sdk", "cli",
		"abi", "arn", "sqs", "sns", "s3", "ec2", "vpc":
		return true
	}
	return false
}

// WriteXHTML renders page to Confluence storage XHTML without performing
// read-diff-write reconciliation. Useful for testing the rendering output
// independently of the full WritePage cycle.
func WriteXHTML(w io.Writer, page ast.Page) error {
	return writeConfluencePage(w, page)
}

// ─────────────────────────────────────────────────────────────────────────────
// XHTML rendering
// ─────────────────────────────────────────────────────────────────────────────

// managedPageMarkerXHTML is the info macro emitted at the top of every
// SourceBridge-managed Confluence page.
const managedPageMarkerXHTML = `<ac:structured-macro ac:name="info">
<ac:rich-text-body>
<p>This page is auto-generated by SourceBridge. Edits made directly may be overwritten unless this block is human-edited.</p>
</ac:rich-text-body>
</ac:structured-macro>
`

// writeConfluencePage renders the full page XHTML including the managed marker.
func writeConfluencePage(w io.Writer, page ast.Page) error {
	// Managed-page marker.
	if _, err := io.WriteString(w, managedPageMarkerXHTML); err != nil {
		return err
	}

	for _, blk := range page.Blocks {
		if err := writeConfluenceBlock(w, blk); err != nil {
			return fmt.Errorf("confluence block %q: %w", blk.ID, err)
		}
	}
	return nil
}

// sb-block round-trip markers. These are HTML comments rather than a custom
// Confluence macro, so the page renders correctly in any Confluence instance
// (no app/macro install required) while still letting the reconciler recover
// per-block ownership on the next write.
//
// Format:
//
//	<!-- sb:block id="..." kind="..." owner="..." -->
//	<plain XHTML for the block>
//	<!-- /sb:block -->
const (
	sbBlockOpenPrefix = "<!-- sb:block"
	sbBlockClosePrefix = "<!-- /sb:block"
	sbBlockClose      = "<!-- /sb:block -->"
)

// writeConfluenceBlock renders one block surrounded by sb-block HTML-comment
// markers and the block's plain XHTML body.
func writeConfluenceBlock(w io.Writer, blk ast.Block) error {
	openComment := fmt.Sprintf(`<!-- sb:block id=%q kind=%q owner=%q -->`,
		xmlEscape(string(blk.ID)),
		xmlEscape(string(blk.Kind)),
		xmlEscape(string(blk.Owner)),
	)
	if _, err := fmt.Fprintln(w, openComment); err != nil {
		return err
	}
	if err := writeConfluenceBlockContent(w, blk.Kind, blk.Content); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w, sbBlockClose); err != nil {
		return err
	}
	return nil
}

// writeConfluenceBlockContent renders one block's content as Confluence XHTML.
func writeConfluenceBlockContent(w io.Writer, kind ast.BlockKind, content ast.BlockContent) error {
	switch kind {
	case ast.BlockKindHeading:
		return writeConfluenceHeading(w, content)
	case ast.BlockKindParagraph:
		return writeConfluenceParagraph(w, content)
	case ast.BlockKindCode:
		return writeConfluenceCode(w, content)
	case ast.BlockKindTable:
		return writeConfluenceTable(w, content)
	case ast.BlockKindCallout:
		return writeConfluenceCallout(w, content)
	case ast.BlockKindEmbed:
		return writeConfluenceEmbed(w, content)
	case ast.BlockKindFreeform:
		return writeConfluenceFreeform(w, content)
	case ast.BlockKindStaleBanner:
		return writeConfluenceStaleBanner(w, content)
	default:
		// Unknown kind — wrap as paragraph so the page doesn't break.
		if _, err := fmt.Fprintf(w, "<p>[unknown block kind: %s]</p>\n", xmlEscape(string(kind))); err != nil {
			return err
		}
		return nil
	}
}

func writeConfluenceHeading(w io.Writer, content ast.BlockContent) error {
	if content.Heading == nil {
		return nil
	}
	tag := headingTagForLevel(content.Heading.Level)
	if err := writeOpenTag(w, tag); err != nil {
		return err
	}
	if err := writeText(w, content.Heading.Text); err != nil {
		return err
	}
	if err := writeCloseTag(w, tag); err != nil {
		return err
	}
	return writeln(w)
}

func writeConfluenceParagraph(w io.Writer, content ast.BlockContent) error {
	if content.Paragraph == nil {
		return nil
	}
	// Convert inline markdown constructs to Confluence XHTML inline tags so
	// Confluence renders them correctly (bold, italic, code, links).
	// Citation handles (path:start-end) are left as-is.
	if err := writeOpenTag(w, "p"); err != nil {
		return err
	}
	xhtml := inlineMarkdownToXHTML(content.Paragraph.Markdown)
	if _, err := fmt.Fprint(w, xhtml); err != nil {
		return err
	}
	if err := writeCloseTag(w, "p"); err != nil {
		return err
	}
	return writeln(w)
}

func writeConfluenceCode(w io.Writer, content ast.BlockContent) error {
	if content.Code == nil {
		return nil
	}
	// Confluence Code Block macro.
	if err := writeAcMacroOpen(w, "code"); err != nil {
		return err
	}
	lang := confluenceCodeLanguage(content.Code.Language)
	if err := writeAcParameter(w, "language", lang); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w); err != nil {
		return err
	}
	// Code body goes in a plain-text body (not rich-text).
	if err := writeOpenTag(w, "ac:plain-text-body"); err != nil {
		return err
	}
	// CDATA wraps code to prevent XML special-character escaping.
	if _, err := fmt.Fprintf(w, "<![CDATA[%s]]>", content.Code.Body); err != nil {
		return err
	}
	if err := writeCloseTag(w, "ac:plain-text-body"); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w); err != nil {
		return err
	}
	return writeAcMacroClose(w)
}

func writeConfluenceTable(w io.Writer, content ast.BlockContent) error {
	if content.Table == nil {
		return nil
	}
	t := content.Table
	if len(t.Headers) == 0 {
		return nil
	}

	if err := writeOpenTag(w, "table"); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w); err != nil {
		return err
	}

	// Header row.
	if err := writeOpenTag(w, "tr"); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w); err != nil {
		return err
	}
	for _, h := range t.Headers {
		if err := writeOpenTag(w, "th"); err != nil {
			return err
		}
		if err := writeText(w, h); err != nil {
			return err
		}
		if err := writeCloseTag(w, "th"); err != nil {
			return err
		}
		if _, err := fmt.Fprintln(w); err != nil {
			return err
		}
	}
	if err := writeCloseTag(w, "tr"); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w); err != nil {
		return err
	}

	// Data rows.
	for _, row := range t.Rows {
		if err := writeOpenTag(w, "tr"); err != nil {
			return err
		}
		if _, err := fmt.Fprintln(w); err != nil {
			return err
		}
		for _, cell := range row {
			if err := writeOpenTag(w, "td"); err != nil {
				return err
			}
			if err := writeText(w, cell); err != nil {
				return err
			}
			if err := writeCloseTag(w, "td"); err != nil {
				return err
			}
			if _, err := fmt.Fprintln(w); err != nil {
				return err
			}
		}
		if err := writeCloseTag(w, "tr"); err != nil {
			return err
		}
		if _, err := fmt.Fprintln(w); err != nil {
			return err
		}
	}

	if err := writeCloseTag(w, "table"); err != nil {
		return err
	}
	return writeln(w)
}

func writeConfluenceCallout(w io.Writer, content ast.BlockContent) error {
	if content.Callout == nil {
		return nil
	}
	// Map callout kind to Confluence macro name.
	macroName := calloutKindToConfluenceMacro(content.Callout.Kind)
	if err := writeAcMacroOpen(w, macroName); err != nil {
		return err
	}
	if err := writeAcRichTextBodyWrap(w, func(inner io.Writer) error {
		if err := writeOpenTag(inner, "p"); err != nil {
			return err
		}
		if err := writeText(inner, content.Callout.Body); err != nil {
			return err
		}
		if err := writeCloseTag(inner, "p"); err != nil {
			return err
		}
		return writeln(inner)
	}); err != nil {
		return err
	}
	return writeAcMacroClose(w)
}

// calloutKindToConfluenceMacro maps callout kind strings to Confluence macro names.
// Confluence has: note, tip, info, warning.
func calloutKindToConfluenceMacro(kind string) string {
	switch strings.ToLower(kind) {
	case "warning", "danger":
		return "warning"
	case "tip":
		return "tip"
	case "note":
		return "note"
	default:
		return "info"
	}
}

func writeConfluenceEmbed(w io.Writer, content ast.BlockContent) error {
	if content.Embed == nil {
		return nil
	}
	// Embeds are represented as a Confluence page include macro.
	if err := writeAcMacroOpen(w, "include"); err != nil {
		return err
	}
	if err := writeOpenTag(w, "ri:page", "ri:content-title", content.Embed.TargetPageID); err != nil {
		return err
	}
	if err := writeCloseTag(w, "ri:page"); err != nil {
		return err
	}
	if _, err := fmt.Fprintln(w); err != nil {
		return err
	}
	if content.Embed.TargetBlockID != "" {
		if err := writeAcParameter(w, "block_id", string(content.Embed.TargetBlockID)); err != nil {
			return err
		}
		if _, err := fmt.Fprintln(w); err != nil {
			return err
		}
	}
	return writeAcMacroClose(w)
}

func writeConfluenceFreeform(w io.Writer, content ast.BlockContent) error {
	if content.Freeform == nil {
		return nil
	}
	// Freeform content is stored as raw text inside a paragraph. Callers
	// writing freeform blocks are responsible for ensuring the raw content is
	// valid XHTML — we do not escape it here.
	if _, err := fmt.Fprintln(w, content.Freeform.Raw); err != nil {
		return err
	}
	return nil
}

func writeConfluenceStaleBanner(w io.Writer, content ast.BlockContent) error {
	if content.StaleBanner == nil {
		return nil
	}
	s := content.StaleBanner
	// Stale banners render as Confluence warning macros.
	if err := writeAcMacroOpen(w, "warning"); err != nil {
		return err
	}
	if err := writeAcRichTextBodyWrap(w, func(inner io.Writer) error {
		if err := writeOpenTag(inner, "p"); err != nil {
			return err
		}
		syms := strings.Join(s.TriggeringSymbols, ", ")
		text := fmt.Sprintf("This page may be out of date. Recent changes to %s (commit %s) may affect this content.", syms, s.TriggeringCommit)
		if s.RefreshURL != "" {
			text += " Refresh from source: " + s.RefreshURL
		}
		if s.NextRegenWindow != "" {
			text += " Next scheduled regen: " + s.NextRegenWindow
		}
		if err := writeText(inner, text); err != nil {
			return err
		}
		if err := writeCloseTag(inner, "p"); err != nil {
			return err
		}
		return writeln(inner)
	}); err != nil {
		return err
	}
	return writeAcMacroClose(w)
}

