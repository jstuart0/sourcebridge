// confluencePageRecord is the in-memory storage for one Confluence page.
type confluencePageRecord struct {
	xhtml      []byte
	properties ConfluenceProperties
}

// MemoryConfluenceClient is a thread-safe in-memory implementation of
// [ConfluenceClient] for use in tests and development.
type MemoryConfluenceClient struct {
	pages    map[string]*confluencePageRecord // externalID → record
	numericIDs map[string]string             // externalID → fake numeric Confluence page ID
	nextID   int
}

// NewMemoryConfluenceClient creates an empty [MemoryConfluenceClient].
func NewMemoryConfluenceClient() *MemoryConfluenceClient {
	return &MemoryConfluenceClient{
		pages:      make(map[string]*confluencePageRecord),
		numericIDs: make(map[string]string),
		nextID:     1000,
	}
}

// GetPage implements [ConfluenceClient].
func (m *MemoryConfluenceClient) GetPage(_ context.Context, externalID string) ([]byte, ConfluenceProperties, error) {
	rec, ok := m.pages[externalID]
	if !ok {
		return nil, nil, nil
	}
	// Return copies so callers can't mutate internal state.
	xhtml := make([]byte, len(rec.xhtml))
	copy(xhtml, rec.xhtml)
	props := make(ConfluenceProperties, len(rec.properties))
	for k, v := range rec.properties {
		props[k] = v
	}
	return xhtml, props, nil
}

// UpsertPage implements [ConfluenceClient].
func (m *MemoryConfluenceClient) UpsertPage(_ context.Context, externalID string, xhtml []byte, metadata ConfluenceProperties) error {
	body := make([]byte, len(xhtml))
	copy(body, xhtml)
	props := make(ConfluenceProperties, len(metadata))
	for k, v := range metadata {
		props[k] = v
	}
	m.pages[externalID] = &confluencePageRecord{xhtml: body, properties: props}
	return nil
}

// GetBlockByExternalID implements [ConfluenceClient] by parsing the stored
// XHTML to find the macro with the matching block ID parameter.
func (m *MemoryConfluenceClient) GetBlockByExternalID(_ context.Context, pageExternalID string, blockExternalID ast.BlockID) ([]byte, bool, error) {
	rec, ok := m.pages[pageExternalID]
	if !ok {
		return nil, false, nil
	}
	blocks := parseConfluenceBlocks(rec.xhtml)
	for _, b := range blocks {
		if b.id == blockExternalID {
			return []byte(b.rawXHTML), true, nil
		}
	}
	return nil, false, nil
}

// ListPagesByExternalIDPrefix implements [ConfluenceClient] by scanning the
// in-memory page map for keys with the given prefix.
func (m *MemoryConfluenceClient) ListPagesByExternalIDPrefix(_ context.Context, prefix string) ([]string, error) {
	var out []string
	for id := range m.pages {
		if strings.HasPrefix(id, prefix) {
			out = append(out, id)
		}
	}
	return out, nil
}

// DeletePage implements [ConfluenceClient] by removing the page from the
// in-memory map. Returns nil when the page does not exist (idempotent).
func (m *MemoryConfluenceClient) DeletePage(_ context.Context, externalID string) error {
	delete(m.pages, externalID)
	delete(m.numericIDs, externalID)
	return nil
}

// EnsurePage implements [ConfluenceClient]. It creates the page if it does not
// already exist, and returns a stable fake numeric Confluence page ID in both
// cases. Existing pages are left unchanged (the body argument is ignored for
// pre-existing pages).
func (m *MemoryConfluenceClient) EnsurePage(_ context.Context, externalID, title string, _ string, body []byte) (string, error) {
	if numID, ok := m.numericIDs[externalID]; ok {
		return numID, nil
	}
	// Create the page.
	cp := make([]byte, len(body))
	copy(cp, body)
	m.pages[externalID] = &confluencePageRecord{
		xhtml: cp,
		properties: ConfluenceProperties{
			"sourcebridge_page_id": externalID,
			propConfluenceTitle:    title,
		},
	}
	m.nextID++
	numID := fmt.Sprintf("%d", m.nextID)
	m.numericIDs[externalID] = numID
	return numID, nil
}

// NumericPageID returns the fake Confluence numeric page ID for the given
// external ID. Returns "" when the page does not exist. Used in tests.
func (m *MemoryConfluenceClient) NumericPageID(externalID string) string {
	return m.numericIDs[externalID]
}

// MutateBlock directly modifies the stored XHTML of a block, simulating a
// Confluence editor changing content. This is used in integration tests to
// inject "human edits" into the in-memory store without going through the
// normal WritePage path.
//
// newBodyXHTML is the XHTML content that replaces the block's rich-text body.
// The block's id, kind, and owner macro parameters are preserved.
func (m *MemoryConfluenceClient) MutateBlock(pageExternalID string, blockID ast.BlockID, newBodyXHTML string) error {
	rec, ok := m.pages[pageExternalID]
	if !ok {
		return fmt.Errorf("memory confluence: page %q not found", pageExternalID)
	}

	// Replace the rich-text body of the block identified by blockID.
	// We do a targeted string replacement of the macro content.
	xhtml := string(rec.xhtml)
	result, err := replaceBlockBody(xhtml, blockID, newBodyXHTML)
	if err != nil {
		return err
	}
	rec.xhtml = []byte(result)
	return nil
}

// replaceBlockBody replaces the body between sb-block open/close comment
// markers for the block whose id attribute matches blockID. Returns the
// modified XHTML or an error if the block is not found.
func replaceBlockBody(xhtml string, blockID ast.BlockID, newBodyXHTML string) (string, error) {
	escapedID := xmlEscape(string(blockID))

	// Locate every sb-block open comment and pick the one whose id attribute
	// matches blockID — attribute order in the comment is not guaranteed, so
	// we cannot match against a single literal substring.
	for _, loc := range reSBBlockOpen.FindAllStringSubmatchIndex(xhtml, -1) {
		attrSpan := xhtml[loc[2]:loc[3]]
		var matched bool
		for _, m := range reSBAttr.FindAllStringSubmatch(attrSpan, -1) {
			if m[1] == "id" && m[2] == escapedID {
				matched = true
				break
			}
		}
		if !matched {
			continue
		}

		openEnd := loc[1]
		closeRel := strings.Index(xhtml[openEnd:], sbBlockClosePrefix)
		if closeRel < 0 {
			break
		}
		closeAbs := openEnd + closeRel
		closeEnd := strings.Index(xhtml[closeAbs:], "-->")
		if closeEnd < 0 {
			break
		}
		closeEnd = closeAbs + closeEnd + len("-->")

		var b strings.Builder
		b.WriteString(xhtml[:openEnd])
		b.WriteString("\n")
		b.WriteString(strings.TrimSpace(newBodyXHTML))
		b.WriteString("\n")
		b.WriteString(sbBlockClose)
		b.WriteString(xhtml[closeEnd:])
		return b.String(), nil
	}

	return "", fmt.Errorf("confluence block %q not found in XHTML", blockID)
}

// extractAcParameter extracts the text content of <ac:parameter ac:name="name">…</ac:parameter>.
func extractAcParameter(xhtml, name string) string {
	pattern := fmt.Sprintf(`<ac:parameter ac:name="%s">`, xmlEscape(name))
	idx := strings.Index(xhtml, pattern)
	if idx < 0 {
		return ""
	}
	start := idx + len(pattern)
	end := strings.Index(xhtml[start:], "</ac:parameter>")
	if end < 0 {
		return ""
	}
	// Unescape the value.
	val := xhtml[start : start+end]
	val = strings.ReplaceAll(val, "&amp;", "&")
	val = strings.ReplaceAll(val, "&lt;", "<")
	val = strings.ReplaceAll(val, "&gt;", ">")
	val = strings.ReplaceAll(val, "&quot;", `"`)
	val = strings.ReplaceAll(val, "&apos;", "'")
	return val
}

